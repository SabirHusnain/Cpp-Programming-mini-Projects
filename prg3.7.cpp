#include <iostream>

using namespace std;

int main()
{
     int b = 1;
     cout << "Value of 'b' before adding '1': " << b << endl;
     b = b + 1;
     cout << "Value of 'b' after adding '1': " << b << endl;
     b++;
     cout << "Value of 'b' after adding '1' again: " << b << endl;
     return 0;
}