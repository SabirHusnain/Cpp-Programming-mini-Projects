#include <iostream>

using namespace std;

int main()
{
     float a, b;
     cout << "Enter first number: ";
     cin >> a;
     cout << "Enter second number: ";
     cin >> b;
     cout << "Vale of a+b is: " << a + b << endl;
     cout << "Vale of a+b is: " << a - b << endl;
     cout << "Vale of a+b is: " << a * b << endl;
     cout << "Vale of a+b is: " << a / b << endl;
     return 0;
}
